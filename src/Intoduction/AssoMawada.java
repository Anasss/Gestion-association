private void DeplacerStagiaire(int i){
    try{
        if (dt.getRowCount() == 0){
            btnPrecedent.setEnabled(false);
            btnSuivant.setEnabled(false);
            txtmat.setText("");
            txtnom.setText("");
            txtprenom.setText("");
            txtNbreFils.setText("");
            txtdateNaiss.setText("");
            txtAdresse.setText("");
            txtTelephone.setText("");
        }else{
            if(i == 0){
                btnPrecedent.setEnabled(false);
                btnSuivant.setEnabled(true);
            }else if (i == dt.getRowCount()-1){ // dernier enregistrement
                btnPrecedent.setEnabled(true);
            btnSuivant.setEnabled(false);
            }else{
                btnPrecedent.setEnabled(true);
                btnSuivant.setEnabled(true);
            }
            if(dt.getRowCount() == 1){
                btnPrecedent.setEnabled(false);
                btnSuivant.setEnabled(false);
            }
        }
        txtmat.setText(dt.getValueAt(i, 0).toString()); // i represente numero d ligne ,et 0 1 .. colonne
        txtnom.setText(dt.getValueAt(i, 1).toString());
        txtprenom.setText(dt.getValueAt(i, 2).toString());
        txtNbreFils.setText(dt.getValueAt(i, 3).toString());
        txtdateNaiss.setText(dt.getValueAt(i, 4).toString());
        txtAdresse.setText(dt.getValueAt(i, 5).toString());
        txtTelephone.setText(dt.getValueAt(i, 6).toString());
    }catch(Exception e){
        // JOptionPane.showMessageDialog(null, "Erreur deplacement\n"+e.getMessage());
    }
}
//          procedure qui Afficher la liste des personnes.
private void AfficherPersonne(){
    try{
        dt.setRowCount(0); // vider la list des persoonnes
        Rs = St.executeQuery("Select * From stg");
        
        while(Rs.next()){
            mat = Rs.getInt("mat");
            nom = Rs.getString("nom");
            prenom = Rs.getString("prenom");
            NbreEnfants = Rs.getInt("NbreEnfants");
            dateNaiss = Rs.getDate("dateNaiss");
            adresse = Rs.getString("Adresse");
            telephone= Rs.getString("Telephone");
            niveauScolaire = Rs.getString("NiveauScolaire");
            situationFamil = Rs.getString("Situation Familiale");
            profe = Rs.getString("Profession");
            states = Rs.getString("State");
            
            Object[] stg = {mat,nom,prenom,NbreEnfants,dateNaiss,adresse,telephone, niveauScolaire,situationFamil,profe,states};
            dt.addRow(stg);
        }
    }catch(Exception e){
        JOptionPane.showMessageDialog(null, "Erreur afficher la liste des Personnes\n"+e.getMessage());
    }
}
