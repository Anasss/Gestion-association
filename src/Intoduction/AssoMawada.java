private void btnAjouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAjouterActionPerformed
    try{
        
        String Adr = txtAdresse.getText();
        String Tel = txtTelephone.getText();
        String Niv = (String)NiveauScolaire.getSelectedItem();
        String SitF = (String)SituationFamil.getSelectedItem();
        String Prof = (String)Profession.getSelectedItem();
        // non affiché sur l'appli dispo sur la bdd :
        String SitH = (String)situationHome.getSelectedItem();
        String Mala = (String)Maladie.getSelectedItem();
        String nbreF = txtNbreFils.getText();
        String filsM = txtfilsMalade.getText();
        String filsE = txtnbreFilsEcolier.getText();
        String EcolP = txtNbrePrimaire.getText();
        String state = (String) jComboBoxState.getSelectedItem();
        String nbDip = txtnbreDiplome.getText();
        String nomFe = txtnomfemme.getText();
        
        // verifier si deja exist
        boolean b = false;
        for(int j=0; j < dt.getRowCount() ;j++){
            if(Integer.parseInt(txtmat.getText()) == dt.getValueAt(j, 0)){
                b = true;
                break;
            }
        }
        if(b == false){
           
            St.executeUpdate("Insert into stg Values("+txtmat.getText()+",'"+txtnom.getText()+"','"+txtprenom.getText()+"',"+txtNbreFils.getText()+",'"+txtdateNaiss.getText()+"','"+Adr+"','"+Tel+"','"+Niv+"','"+SitF+"','"+Prof+"','"+SitH+"','"+Mala+"','"+nbreF+"','"+filsM+"','"+filsE+"','"+EcolP+"','"+state+"','"+nbDip+"','"+nomFe+"')");
            AfficherPersonne(); // recharger la liste des stagiaires
            JOptionPane.showMessageDialog(null, "La personné a été ajouté avec succés");
        }else{
            JOptionPane.showMessageDialog(null, "matricule déja existe !");
            txtmat.setText("");
        }
    }catch(Exception e){
        JOptionPane.showMessageDialog(null, "Erreur lors de l'ajout \n"+e.getMessage());
    }
}//GEN-LAST:event_btnAjouterActionPerformed
